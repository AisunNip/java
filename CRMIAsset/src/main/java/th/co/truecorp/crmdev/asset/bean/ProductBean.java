package th.co.truecorp.crmdev.asset.bean;

public class ProductBean {

	private String productRowID;
	private String productName;
	private String productPartNo;
	private String productLine;
	private String productType;
	private String productSubType;
	private String productCategory;
	private String productCode;
	
	public ProductBean() {
	}

	public String getProductRowID() {
		return productRowID;
	}

	public void setProductRowID(String productRowID) {
		this.productRowID = productRowID;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductPartNo() {
		return productPartNo;
	}

	public void setProductPartNo(String productPartNo) {
		this.productPartNo = productPartNo;
	}

	public String getProductLine() {
		return productLine;
	}

	public void setProductLine(String productLine) {
		this.productLine = productLine;
	}

	public String getProductType() {
		return productType;
	}

	public void setProductType(String productType) {
		this.productType = productType;
	}

	public String getProductSubType() {
		return productSubType;
	}

	public void setProductSubType(String productSubType) {
		this.productSubType = productSubType;
	}

	public String getProductCategory() {
		return productCategory;
	}

	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}

	public String getProductCode() {
		return productCode;
	}

	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
}